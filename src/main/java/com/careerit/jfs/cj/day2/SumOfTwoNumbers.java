package com.careerit.jfs.cj.day2;

public class SumOfTwoNumbers {

  public static void main(String[] args) {
    int num1 = 20;
    int num2 = 3;
    System.out.println(num1 + " + " + num2 + " = " + (num1 + num2));
    System.out.println(num1 + " - " + num2 + " = " + (num1 - num2));
    System.out.println(num1 + " * " + num2 + " = " + (num1 * num2));
    System.out.println(num1 + " / " + num2 + " = " + (num1 / num2));
    System.out.println(num1 + " % " + num2 + " = " + (num1 % num2));
  }
}
